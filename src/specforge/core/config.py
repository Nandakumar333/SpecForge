def get_constitution_vars() -> TemplateVarSchema:
    """Variable schema for constitution templates."""
    from specforge.core.template_models import TemplateVarSchema

    return TemplateVarSchema(
        required={
            "project_name": str,
            "agent": str,
            "stack": str,
            "date": str,
            "stack_hint": str,
        },
    )


def get_prompt_vars() -> TemplateVarSchema:
    """Variable schema for prompt templates."""
    from specforge.core.template_models import TemplateVarSchema

    return TemplateVarSchema(
        required={
            "project_name": str,
            "agent": str,
            "stack": str,
            "date": str,
            "stack_hint": str,
        },
        optional={
            "conventions": (str, ""),
            "patterns": (str, ""),
        },
    )


def get_feature_vars() -> TemplateVarSchema:
    """Variable schema for feature templates."""
    from specforge.core.template_models import TemplateVarSchema

    return TemplateVarSchema(
        required={
            "project_name": str,
            "date": str,
        },
        optional={
            "feature_name": (str, ""),
            "stack": (str, "agnostic"),
            "stack_hint": (str, "Language-agnostic"),
        },
    )
